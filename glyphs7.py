def width(name: str) -> int:
    g = GLYPHS7[name]
    return max(len(r) for r in g)


def normalized(name: str) -> list[str]:
    """Pad each row to full glyph width."""
    g = GLYPHS7[name]
    w = width(name)
    return [r.ljust(w, ".") for r in g]
